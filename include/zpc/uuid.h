constexpr size_t UUID_BYTE_SIZE   = 16U;
constexpr size_t UUID_STRING_SIZE = 37U;

typedef struct uuid_t
{
    uint8_t bytes[UUID_BYTE_SIZE];
} uuid_t;

typedef struct uuid_str_t
{
    char str[UUID_STRING_SIZE];
} uuid_str_t;

// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
bool uuid_is_nil_z(const uuid_t uuid);
uuid_t uuid_nil_z();
