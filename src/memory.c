#include "zpc/memory.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
void* malloc_z(size_t size)
{
    void* ptr = malloc(size);
    if (!ptr)
    {
        fprintf(stderr, "malloc_z: failed to allocate %zu bytes\n", size);
        exit(1);
    }
    return ptr;
}

// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
void* calloc_z(size_t num, size_t size)
{
    void* ptr = calloc(num, size);
    if (!ptr)
    {
        fprintf(stderr, "calloc_z: failed to allocate %zu * %zu bytes\n", num, size);
        exit(1);
    }
    return ptr;
}

// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
char* strdup_z(const char* str)
{
    size_t len = strlen(str) + 1;
    char* copy = malloc_z(len);
    memcpy(copy, str, len);
    return copy;
}

// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
// =========================================================================================================================================
void free_z(void* ptr)
{
    free(ptr);
}
